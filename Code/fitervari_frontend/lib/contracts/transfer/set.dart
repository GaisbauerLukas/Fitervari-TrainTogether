import 'package:flutter/cupertino.dart';

class Set {
  int repetitions;
  double distance;
  double weight;
  double time;
  int setNumber;
  String type;

  Set({
    @required this.repetitions,
    @required this.distance,
    @required this.weight,
    @required this.time,
    @required this.setNumber,
    @required this.type
  });
}